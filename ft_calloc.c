void	*ft_calloc(size_t count, size_t size)
{
	void	*p;
	size_t	i;
	
	p = (void *)malloc(sizeof(void*) * size);
	i = 0;
	while (i < size)
	{
		p[i] = 0;
		i++;
	}
	return (p);
}
