#include "libft.h"

void				*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*p;

	p = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		if ((unsigned char)c == p[i])
			return (p + i);
		i++;
	}
	return ((void*)0);
}
