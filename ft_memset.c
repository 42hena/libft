#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*p;

	b = p;
	i = 0;
	while (i < len)
	{
		*(b + i) = c;
		i++;
	}
	return (b);
}
